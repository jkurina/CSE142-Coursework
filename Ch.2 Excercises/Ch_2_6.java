// Print number pyrimid using for loops

public class Ch_2_6 {
   public static void main(String[] args) {
      for (int lines=1; lines<=7; lines++) {
         for (int j=1; j<=lines; j++) {  
               System.out.print(lines + "");          
         }
         System.out.println();
      }
   }
}